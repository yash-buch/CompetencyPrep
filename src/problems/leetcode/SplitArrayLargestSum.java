    public int splitArray(int[] nums, int m) {
        int[][] dp = new int[nums.length][m];
        int[] cumSum = new int[nums.length];
        cumSum[0] = nums[0];
        for(int i = 1; i < nums.length; i++) {
            cumSum[i] = cumSum[i-1]+nums[i];
        }
        return recur(nums, 0, m-1, dp, cumSum);
    }

    private int recur(int[] nums, int index, int m, int[][] dp, int[] cumSum) {
        if (m == 0) {
            dp[index][m] = cumSum[nums.length-1]-cumSum[index]+nums[index];
            return dp[index][m];
        }
        if (dp[index][m] != 0) {
            return dp[index][m];
        }

        int minSum = Integer.MAX_VALUE;
        for (int i = index; i < nums.length-m; i++) {
            int _minSum = recur(nums, i+1, m-1, dp, cumSum);
            _minSum = Math.max(_minSum, cumSum[i]-cumSum[index]+nums[index]);
            minSum = Math.min(minSum, _minSum);
        }
        dp[index][m] = minSum;
        return minSum;
    }
